def L(nid, speaker, text, nxt=None, mode=None, require=None):
    n = {"id": nid, "type": "line", "speaker": speaker, "text": text}
    if nxt: n["next"] = nxt
    if mode: n["mode"] = mode
    if require: n["require"] = require
    return n

def S(nid, text, nxt=None, effects=None, require=None):
    n = {"id": nid, "type": "system", "speaker": "system", "text": text}
    if effects: n["effects"] = effects
    if nxt: n["next"] = nxt
    if require: n["require"] = require
    return n

def C(nid, prompt, options, mode=None, nxt=None):
    n = {"id": nid, "type": "choice", "speaker": "system", "text": prompt, "options": options}
    if mode: n["mode"] = mode
    if nxt: n["next"] = nxt  # mode/require 不符時之跳過路徑
    return n

def O(oid, text, nxt, effects=None, require=None, mode=None):
    o = {"id": oid, "text": text, "next": nxt}
    if effects: o["effects"] = effects
    if require: o["require"] = require
